#include <iostream>

int main() {
    /*
        std     -> référence à la librairie standard
        cout    -> variable globale contenant le flux pour écrire sur la sortie standard
        <<      -> opérateur, indique que la variable qui suit sera écrite dans le flux
        endl    -> variable globale contenant le caractère de fin de ligne \n                
    */
    std::cout << "Hello world!" << std::endl;
    return 0;
}